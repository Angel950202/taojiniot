package com.taojin.iot.agreement.fujiya.entity;

import javax.persistence.Entity;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

import com.taojin.iot.base.comm.entity.BaseEntity;


/**
 * Entity-指令库-RC70-1
 * @author wangjie
 *
 */
@Entity
@Table(name = "iot_agreement_rc701")
@SequenceGenerator(name = "sequenceGenerator", sequenceName = "iot_agreement_rc701_sequence")
public class AgreementRc701 extends BaseEntity{

	private static final long serialVersionUID = -9137348902764358298L;
	
	/**信号名称*/
	private String name;
	/**信号地址*/
	private String address;
	/**信号类型*/
	private String type;
	/**命令值*/
	private String commandValue;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCommandValue() {
		return commandValue;
	}
	public void setCommandValue(String commandValue) {
		this.commandValue = commandValue;
	}
}
